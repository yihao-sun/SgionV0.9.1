def build_distribution(phase: int) -> dict:
    """根据主导相位生成分布（主导0.7，其他各0.1）"""
    dist = {0: 0.1, 1: 0.1, 2: 0.1, 3: 0.1}
    dist[phase] = 0.7
    return dist

def main():
    seed_data = {}
    
    # 先处理非中性类别，确保优先使用非中性分类
    # 相位 0 词条
    for w in WORDS[0]:
        if w not in seed_data:
            seed_data[w] = {"phase_distribution": build_distribution(0), "confidence": 0.5}
    # 相位 1 词条
    for w in WORDS[1]:
        if w not in seed_data:
            seed_data[w] = {"phase_distribution": build_distribution(1), "confidence": 0.5}
    # 相位 2 词条
    for w in WORDS[2]:
        if w not in seed_data:
            seed_data[w] = {"phase_distribution": build_distribution(2), "confidence": 0.5}
    # 相位 3 词条
    for w in WORDS[3]:
        if w not in seed_data:
            seed_data[w] = {"phase_distribution": build_distribution(3), "confidence": 0.5}
    # 处理中性类别，只添加未在其他类别中出现的词
    for w in WORDS["neutral"]:
        if w not in seed_data:
            seed_data[w] = {"phase_distribution": {0:0.25, 1:0.25, 2:0.25, 3:0.25}, "confidence": 0.3}
    
    # 保存
    output_path = Path("data/semantic_seed.json")
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(seed_data, f, ensure_ascii=False, indent=2)
    
    print(f"生成 {len(seed_data)} 个词条的纯数字相位语义库")

if __name__ == "__main__":
    main()